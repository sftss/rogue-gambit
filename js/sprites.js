// ==================== SPRITE DRAWING ====================

function drawSprite(ctx, spriteKey, team, x, y, tileSize) {
  var sprite = SPRITES[spriteKey];
  if (!sprite) return;

  var margin = 4;
  var drawSize = tileSize - margin * 2;
  var pixelW = drawSize / 12;
  var pixelH = drawSize / 12;
  var startX = x + margin;
  var startY = y + margin;

  // Build color palette based on team
  var isWhite = (team === TEAM.WHITE);

  var primary   = isWhite ? COLORS.WHITE_PRIMARY   : COLORS.BLACK_PRIMARY;
  var secondary = isWhite ? COLORS.WHITE_SECONDARY : COLORS.BLACK_SECONDARY;
  var outline   = isWhite ? COLORS.WHITE_OUTLINE   : COLORS.BLACK_OUTLINE;
  var accent    = ACCENT_COLORS[spriteKey] || (isWhite ? '#c0b090' : '#605070');
  var highlight = isWhite ? '#ffffff' : '#a898b0';
  var shadow    = isWhite ? '#685840' : '#100818';

  // Extra accent '7' varies by piece
  var extra;
  if (spriteKey === PT.PINATA) {
    extra = isWhite ? '#44dd44' : '#22aa22';
  } else {
    extra = accent;
  }

  var colorMap = {
    '1': primary,
    '2': secondary,
    '3': accent,
    '4': outline,
    '5': highlight,
    '6': shadow,
    '7': extra,
  };

  for (var row = 0; row < 12; row++) {
    var line = sprite[row];
    for (var col = 0; col < 12; col++) {
      var ch = line[col];
      if (ch === '.') continue;
      var color = colorMap[ch];
      if (!color) continue;
      ctx.fillStyle = color;
      ctx.fillRect(
        Math.floor(startX + col * pixelW),
        Math.floor(startY + row * pixelH),
        Math.ceil(pixelW),
        Math.ceil(pixelH)
      );
    }
  }
}
